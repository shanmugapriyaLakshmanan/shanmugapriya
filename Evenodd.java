import java.util.Scanner;
public class Evenodd {
	public static void main(String[] args) {
		int n;
		Scanner in=new Scanner(System.in);
		n=in.nextInt();
		if(n>=1 && n<=100000){
		 if(n%2==0){
			System.out.println("even");
		}
		else{
			System.out.println("odd");
		}
		}
		else{
			System.out.println("Invalid no");
		}
	}
}
